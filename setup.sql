-- ─────────────────────────────────────────────────────────────
--  TABLA PRINCIPAL: pokemon
-- ─────────────────────────────────────────────────────────────
CREATE TABLE pokemon (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(50) NOT NULL UNIQUE,
    tipo_principal VARCHAR(20) NOT NULL,
    nivel INT DEFAULT 1 CHECK (
        nivel BETWEEN 1 AND 100
    ),
    ataque INT DEFAULT 0 CHECK (ataque >= 0),
    defensa INT DEFAULT 0 CHECK (defensa >= 0),
    velocidad INT DEFAULT 0 CHECK (velocidad >= 0)
);
-- ─────────────────────────────────────────────────────────────
--  TABLA: gimnasios
-- ─────────────────────────────────────────────────────────────
CREATE TABLE gimnasios (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    ciudad VARCHAR(100) NOT NULL,
    tipo_especialidad VARCHAR(20) NOT NULL,
    num_medallas INT DEFAULT 0
);
-- ─────────────────────────────────────────────────────────────
--  TABLA: entrenadores
-- ─────────────────────────────────────────────────────────────
CREATE TABLE entrenadores (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    medallas INT DEFAULT 0,
    dinero INT DEFAULT 3000,
    id_gimnasio INT UNSIGNED NULL,
    fecha_registro DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_gimnasio) REFERENCES gimnasios(id) ON DELETE
    SET NULL
);
-- ─────────────────────────────────────────────────────────────
--  TABLA: pokemon_entrenador (Pokémon capturados)
--  Relación M:N entre entrenadores y pokémon
-- ─────────────────────────────────────────────────────────────
CREATE TABLE pokemon_entrenador (
    id INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
    id_entrenador INT UNSIGNED NOT NULL,
    id_pokemon INT UNSIGNED NOT NULL,
    nivel_captura INT DEFAULT 1,
    apodo VARCHAR(50) NULL,
    en_equipo BOOLEAN DEFAULT TRUE,
    fecha_captura DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (id_entrenador) REFERENCES entrenadores(id) ON DELETE CASCADE,
    FOREIGN KEY (id_pokemon) REFERENCES pokemon(id) ON DELETE CASCADE
);
