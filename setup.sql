CREATE OR REPLACE VIEW vista_top_competitivo AS
SELECT id,
    nombre,
    tipo_principal,
    nivel,
    ataque,
    defensa,
    velocidad,
    (ataque + defensa + velocidad) AS total_base,
    RANK() OVER (
        ORDER BY (ataque + defensa + velocidad) DESC
    ) AS ranking
FROM pokemon
ORDER BY total_base DESC
LIMIT 10;
